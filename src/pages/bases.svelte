	<div>
		<h1 class="2xl:text-5xl lg:text-sm font-bowlby text-pet-blue">1. PRESENTACIÓN</h1>
		<div class="py-8 max-w-3xl text-lg font-free">
			<p>
				Desde Democracia en Red y el Observatorio de Redes,
				con el apoyo del Instituto Nacional Demócrata (NDI)
				y la Carrera de Ciencias Políticas de la Universidad de Buenos Aires (UBA)
				queremos invitarlos/as a contribuir en la explotación del repositorio
				<b>Políticxs en Twitter</b> con el fin de <b>producir conocimiento a partir de datos</b>.
			</p>
			<br>
			<p>
				<b>Politicxs en Twitter</b> es un repositorio que nuclealas interacciones en Twitter
				de más de 500 usuarios relacionados con la política. Ponemos a disposición
				de la comunidad este atlas, con datos abiertos y reutilizables para su libre uso.
				A través de esta iniciativa, buscamos generar un espacio de difusión del conocimiento
				y la investigación en relación a los comportamientos de los políticos
				y políticas del país en la red social Twitter.
			</p>	
		</div>
	</div>
	<div>
		<h1 class="2xl:text-5xl lg:text-sm font-bowlby text-pet-blue">2. CONVOCATORIA #DigamosTodo</h1>
		<div class="py-8 max-w-3xl text-lg font-free">
			<p>
				La presente convocatoria busca incentivar la investigación basada en datos,
				enfocada en la temática de desinformación en la Argentina. Buscamos propuestas
				que potencien el uso de los datos puestos a disposición en <b>Politicxs en Twitter</b>
				y analicen, de forma original e innovadora, la responsabilidad de políticos y políticas
				en Argentina en las dinámicas de la desinformación.
			</p>
			<br>
			<p>
				La desinformación es un fenómeno político a través del cual se intenta, con diferentes intereses,
				afectar la conversación pública a partir de la inclusión de mensajes y/o contenidos que no reflejan la realidad.
				Así, se crean artículos, sitios web y/o publicaciones deliberadamente engañosos que, con frecuencia,
				procuran infringir un daño en el oponente político.
			</p>
			<br>
			<p>	
				Consideramos relevante promover la discusión en torno a cómo nos informamos, cómo dirimimos
				entre contenido legítimo y desinformación, cuáles pueden ser consideradas fuentes verídicas
				dentro de la red social Twitter y quién lo define. En ese sentido, mucho se dice sobre el rol
				de los medios de comunicación y del comportamiento inauténtico organizado, incluyendo “trolls”,
				que impulsan la desinformación. Pero poco se habla de la actitud de nuestros representantes.
			</p>
			<br>
			<p>
				La <b>presente convocatoria apunta, específicamente, a construir evidencia sobre el rol que juegan
				los políticos y políticas del país en la proliferación de la desinformación</b>:
				¿lxs politcxs desinforman? ¿de qué forma? ¿podemos dilucidar sus intenciones al hacerlo?
				¿lo hacen de manera sistemática? ¿incurren por error? ¿a qué partidos pertenecen?
				¿hay politicxs que se comportan como “trolls”? ¿quiénes son?
			</p>
		</div>
	</div>
	<div>
		<h1 class="2xl:text-5xl lg:text-sm font-bowlby text-pet-blue">
			3. CONDICIONES DE PARTICIPACIÓN
		</h1>
		<div class="py-8 max-w-3xl text-lg font-free">
			<p>
				La convocatoria está abierta para cualquier persona mayor de 18 años,
				de cualquier lugar del país. La participación podrá ser a título
				de autor individual o de coautores. En ambos casos solo podrá
				presentarse una idea por autor o coautores participantes.
				En caso de autoría colectiva, todos los involucrados deberán
				aprobar la postulación de la propuesta.				
			</p>
			<br>
			<p>
				Apuntamos a que participen de esta convocatoria perfiles
				de investigación: estudiantes, profesionales, académicos, cientistas
				y visualizadores de datos vinculados al mundo de Ingeniería de Datos
				aplicada a las Ciencias Sociales.
			</p>
			<br>
			<p>
				Es una condición para participar la utilización de los datos
				facilitados en el repositorio de <b>Políticxs en Twitter</b>.
				Cualquier contenido o idea que sea copiada de otras obras,
				dándolo a entender como propio y sin adjudicar de forma explícita
				que no es de su autoría, será considerado como plagio.
				En caso de que se detecte plagio en la parcialidad o en la totalidad
				de la idea, la misma quedará automáticamente descalificada
				de la convocatoria. 
			</p>
			<br>
			<p>
				La convocatoria constará de 4 (cuatro) <b>instancias</b> que son:
			</p>
			<br>
			<ol type="a">
				<li>
					<b>A.</b> Recolección de propuestas
					<br>
					<br>
					<p>
            Se abrirá un
            <a href="https://preguntarparaacordar.typeform.com/to/gVqxiRR6"
            target="_blank">
              formulario en línea
            </a>
            en el que deberán presentarse las propuestas en idioma español.
            En ese formato, se deberán incluir los siguientes elementos
            listados a continuación de forma obligatoria:
					</p>
					<br>
					<ul>
						<li>
							· Nombre del participante(s)
						</li>
						<li>
							· Nombre de un representante del equipo
						</li>
						<li>
							· Contacto de correo electrónico
						</li>
						<li>
							· Antecedentes de la/s persona/s solicitantes para conocer en
							profundidad las cualidades y experiencia relevante en materia
							de explotación de datos o relacionada con el tema de la
							convocatoria. 
						</li>
						<li>
							· Título de la propuesta
						</li>
						<li>
							· Un resumen o abstract de la propuesta de hasta 500 palabras
							como máximo, incluyendo una breve propuesta metodológica. 
						</li>
					</ul>
					<br>
					<p>
						Las propuestas serán evaluadas según los siguientes <b>criterios</b>:
					</p>
					<br>
					<ul>
						<li>
							· Pertinencia con respecto al tema de la convocatoria	
						</li>
						<li>
							· Claridad y precisión en la redacción
						</li>
						<li>
							· Originalidad
						</li>
						<li>
							· Rigurosidad en el uso de los datos
						</li>
						<li>
							· El aporte específico al campo de conocimiento 
						</li>
					</ul>
				</li>
				<li>
					<br>
					<b>B.</b> Preselección de propuestas
					<br><br>
					<p>
						Todas las propuestas serán evaluadas por un “Jurado de Evaluación”,
						integrado por un panel 7 (siete) personas, con capacidades
						y conocimientos técnicos acordes. El jurado preseleccionará,
						basándose en los criterios ya mencionados,
						<b>
							3 (tres) propuestas
						</b>
						con el potencial para ganar la convocatoria. 
					</p>
				</li>
				<li>
					<br>
					<b>C.</b> Presentación de las propuestas preseleccionadas
					<br><br>
					<p>
						Lx/s autor/es de las propuestas preseleccionadxs deberán
						presentarse en una instancia virtual, en fecha a convenir,
						en la que contarán con <b>10 (diez) minutos</b> máximo para
						exponer su idea, mostrar las primeras “visualizaciones piloto”
						y explicar cómo llevarán a cabo su proyecto de investigación.
						En esta instancia, se evaluará la capacidad de pitcheo,
						la originalidad de la presentación y el marco técnico brindado. 
					</p>
					<p>
						Las instrucciones sobre la Presentación
						(fecha, formato, modalidad, etc.)
						serán enviadas a la casilla de correo electrónico
						brindada en el formulario, con la debida antelación. 
					</p>
				</li>
				<li>
					<br>
					<b>D.</b> Implementación de la idea ganadora
					<br><br>
					<p>
						La idea ganadora recibirá un premio de 140.000
						(ciento cuarenta mil pesos) que financiará el desarrollo
						de la idea presentada hacia un proyecto de investigación. 
					</p>
					<p>
						El proyecto de investigación deberá contar
						de forma obligatoria con:
					</p>
					<br>
					<ul>
						<li>
							· <b>Cuerpo de análisis y desarrollo.</b> Con un máximo
							de 7.000 palabras (incluidas notas al pie y bibliografía).
							El formato deberá ser de páginas de tamaño A4, con márgenes
							de 3 cm. Párrafos sangrados y justificados, con interlineado
							de 1,5 puntos, sin doble salto de línea entre párrafos.
							Texto en tipografía serif (Times New Roman u otra),
							con cuerpo de 12 puntos.
							<p>
								Para las citas, se solicita atenerse al
								<a href="http://iigg.sociales.uba.ar/wp-content/uploads/sites/22/2020/11/LaCita4taed_10_11_20.pdf">
									estilo APA
								</a>.
							</p>
						</li>
						<br>
						<li>
							· <b>Código y csv trabajados.</b> Sólo se aceptarán
							investigaciones que hayan creado sus códigos en los programas
							RStudio o Python. La entrega del  código deberá hacerse en
							formato txt., especificando qué programa se utilizó para
							realizarlo, en .py o un .R. En ambos casos, deberán entregarse
							todos los archivos (pueden ser txt, csv, excel, geojson, rdata,
							pkl, etc)  que se utilizaron para hacer la investigación.
							El código debe estar completo, debe ser autosuficiente
							y autoexplicativo. Es decir,  debe poder correrse sin ningún
							tipo de inconveniente al momento de la evaluación por parte
							del Jurado y debe estar bien documentado. En el caso de qué\
							sea posible deberá estar todo el proyecto -con los respectivos
							archivos- subidos a Github ó Gitlab. 
						</li>
						<br>
						<li>
							· <b>Visualizaciones.</b> El cuerpo de análisis y desarrollo
							entregado deberá estar basado en visualizaciones de datos claras
							y concisas, que muestren relaciones de interconexión entre
							las variables analizadas. Las visualizaciones tienen que ser
							originales y de autoría propia.
						</li>
					</ul>
				</li>		
			</ol>		
		</div>
	</div>
