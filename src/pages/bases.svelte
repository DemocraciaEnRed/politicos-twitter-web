		<h1 class="2xl:text-5xl lg:text-sm font-bowlby text-pet-blue">
			6. DERECHOS Y PUBLICACIONES
		</h1>
		<div class="py-8 max-w-3xl text-lg font-free">
			<p>
				Esta convocatoria no tiene ningún tipo de fin de lucro o rédito económico
				sino que sus únicos fines son la generación de conocimiento colectivo
				en materia académica y de investigación para su divulgación. 
			</p>
			<br>
			<p>
				La actual convocatoria se enmarca en el movimiento de Ciencia Abierta.
				Buscamos producir conocimiento de manera abierta y colaborativa
				dejando en libre disponibilidad tanto los instrumentos de trabajo
				como los resultados intermedios y finales que se obtienen a lo largo
				de ese proceso. Es en esta línea que las organizaciones encargadas
				de la convocatoria ponen a libre disposición y completa accesibilidad
				datos como recursos compartidos para que sean trabajados por cualquier
				persona interesada. 
			</p>
			<br>
			<p>
				El objetivo es que los y las participantes de la convocatoria
				se sientan cómodxs compartiendo sus propuestas y los eventuales
				resultados finales de sus investigaciones. Sin embargo, compartir
				sus propuestas, documentos y resultados en una comunidad abierta
				no será obligatorio. Las organizaciones encargadas de la convocatoria
				garantizan la protección de los derechos de propiedad intelectual,
				es decir, que no podrán apropiarse, adaptar o publicar libremente
				las propuestas
				<b>
					sin autorización previa de su o sus autores
				</b>
				y sin establecer de forma clara los créditos de autoría pertinentes.
			</p>
			<br>
			<p>
				Todos las propuestas recolectadas para la convocatoria son propiedad
				exclusiva de sus autores y participar de la misma no requiere la cesión
				total o parcial de éstas. 
			</p>		
